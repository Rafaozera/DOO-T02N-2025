public class Vendedor {
    public String nome;
    public int idade;
    public String loja;
    public Endereco endereco;

    public Vendedor(String nome, int idade, String loja, Endereco endereco) {
        this.nome = nome;
        this.idade = idade;
        this.loja = loja;
        this.endereco = endereco;
    }
}